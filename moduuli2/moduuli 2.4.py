a = int(input('Kirjoita ensimmäinen luku: '))
b = int(input('Kirjoita toinen luku: '))
c = int(input('Kirjoita kolmas luku: '))

summa = a + b + c
tulo = a * b * c
keskiarvo = (a + b + c) / 3

print('Lukujen summa on', summa, '\nLukujen tulo on', tulo, '\nLukujen keskiarvo on', int(keskiarvo))